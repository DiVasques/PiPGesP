class User {
  String? _uid;
  String? _displayName;
  String? _phoneNumber;
  String? _email;
  String? _registration;

  String? get uid => _uid;
  String? get displayName => _displayName;
  String? get phoneNumber => _phoneNumber;
  String? get email => _email;
  String? get registration => _registration;

  set uid (value) => _uid;
  set displayName (value) => _displayName;
  set phoneNumber (value) => _phoneNumber;
  set email (value) => _email;
  set registration (value) => _registration;
}
